    {
        if (Cache::has('comunas')) {
            $comunas = Cache::get('comunas');
        } else {
            $comunas = Cache::remember('comunas', 'forever', function () {
                $comunas = Comuna::with('region', 'zona.crm')->orderBy('nombre_comuna', 'asc')->get();
                return $comunas;
            });
        }
